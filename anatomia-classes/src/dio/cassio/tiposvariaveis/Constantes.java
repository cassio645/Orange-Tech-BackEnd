package dio.cassio.tiposvariaveis;

public class Constantes {
    public static void main(String[] args) {
        
        int valor = 5;
        valor = 10;
        System.out.println(valor); // 10

        // Criando constantes(variaveis imutaveis) com a palavra final
        final double PI = 3.14;

        //PI = 50; The final local variable PI cannot be assigned.
        System.out.println(PI);


    }
    
}
