package dio.cassio.operadores;

public class Incremento {
    public static void main(String[] args) {
        int numero = 5;

        numero++; //6
        System.out.println(numero);

        numero += 4; // 10
        System.out.println(numero); 

        numero--; // 9
        System.out.println(numero);

        numero *= 2; // 18
        System.out.println(numero);
    }
}
